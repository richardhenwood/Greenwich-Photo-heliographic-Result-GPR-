        my $beginTime = $columns[0] . " " .  substr($columns[2], 0,2) . ":00";
        $beginTime =~ s/\//-/g;
        push (@epochsParsed, $beginTime);
        #print "date str = $beginTime\n"; 
    }
    return \@epochsParsed;
}

    

